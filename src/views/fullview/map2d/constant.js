export const getFlyZoom = (grid) => {
  const level = grid.level
  let zoom = 14
  switch (level) {
    case 4:
      zoom = 11
      break
    case 5:
      zoom = 13
      break
    case 6:
      zoom = 15
      break
    case 7:
      zoom = 15
      break
  }
  return zoom
}

