export const getLayerStyle = (grid, mapType = 3) => {
  const level = grid.level
  const EARTH = 2
  const DARK = 3

  let colorMap
  // EARTH这里颜色不对
  if (mapType === DARK) colorMap = { 5: '#00A8E6', 6: '#FF4333', 7: '#99FF66' }
  if (mapType === EARTH) colorMap = { 5: '#004466', 6: '#FF4333', 7: '#00CC00' }
  const style = {
    color: mapType === DARK ? (colorMap[level] || '#00D4FF') : '#fff',
    weight: 1,
    fillColor: colorMap[level] || '#00D4FF',
    fillOpacity: mapType === DARK ? 0.16 : 0.3
  }
  return style
}

